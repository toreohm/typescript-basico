/*¿TypeScript es diferente a JavaScript? 
¿Un desarrollador en TypeScript es diferente a uno en JavaScript? 
La respuesta a ambas es sí.

Panorama
En JavaScript solo te das cuenta de que tienes un error hasta el momento en que lo ejecutas, 
sea en el navegador o en un entorno de ejecución como NodeJS, más no antes.

El aporte de TypeScript
TypeScript abarca todo lo que tiene JavaScript, más las nuevas versiones de ECMAScript, 
y añade análisis estático a nuestro código. 
"Entre más rápido encuentres un error, más fácil será solucionarlo"

✅ Análisis de código estático: corre en el editor de código en busca de un typo 
(error en la escritura de un término), llamadas incorrectas a funciones y brinda 
autocompletado de código.

✅ Pruebas Unitarias (Unit Tests): se realiza pruebas para verificar si una parte del código 
hace lo que queremos que ejecute.

✅ Pruebas de Integración (Integration Tests): vemos como todo el código funciona en conjunto 
y que se ejecute cómo deseamos.

✅ Revisión de código (Code Review): se verifica si se ha seguido con las normas, 
estándares y mejores prácticas establecidas por el equipo.

ESNext es un nombre que siempre indica la próxima versión de JavaScript.
*/


